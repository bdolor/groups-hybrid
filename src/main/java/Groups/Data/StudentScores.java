	/**
	 * Given a studentId, sums up the score for that student
	 * 
	 * @param studentId
	 * @return
	 */
	protected int getSumScore(int studentId) {
		int scores[] = StudentScores.Scores[studentId];
		int sum = 0;
		for (int s : scores) {
			sum += s;
		}
		return sum;
	}

	/**
	 * Returns the score of student with studentId
	 * 
	 * @param studentId
	 * @return
	 */
	protected int[] getScores(int studentId) {
		return StudentScores.Scores[studentId];
	}

	/**
	 * Given four student IDs, return the GH value of the group
	 * 
	 * @param student1
	 * @param student2
	 * @param student3
	 * @param student4
	 * @return 
	 */
	public double getGhValue(int student1, int student2, int student3, int student4) {
		double[] sorted = {this.getSumScore(student1), this.getSumScore(student2), this.getSumScore(student3), this.getSumScore(student4)};
		this.sort(sorted);

		double adValue = (sorted[0] + sorted[3]) / 2;
		double ghValue = (sorted[3] - sorted[0])
			/ (1 + ( Math.abs(adValue - sorted[1]) + Math.abs(adValue - sorted[2]) ));
		return ghValue;
	}

	public void sort(double[] sorted) {

		double tmp;
		for (int i = 0; i < sorted.length - 1; i++) {
			if (sorted[i] > (sorted[i + 1])) {
				tmp = sorted[i];
				sorted[i] = sorted[i + 1];
				sorted[i + 1] = tmp;
				sort(sorted);
			}

		}
	}
	
	/**
	 * Given four student IDs return the maximum Euclidean Distance of that group
	 * 
	 * @param student1
	 * @param student2
	 * @param student3
	 * @param student4
	 * @return 
	 */
	public double getMaxDistance(int student1, int student2, int student3, int student4) {
//		ArrayList<Double> distances = new ArrayList<Double>();
		double max = 0.0;
		double tmpMax1 = this.getDistance(student1, student2);
		double tmpMax2 = this.getDistance(student1, student3);
		double tmpMax3 = this.getDistance(student1, student4);
		double tmpMax4 = this.getDistance(student2, student3);
		double tmpMax5 = this.getDistance(student2, student4);
		double tmpMax6 = this.getDistance(student3, student4);
		
		max = (tmpMax1 > tmpMax2) ? tmpMax1 : tmpMax2; 
		max = (tmpMax3 > max) ? tmpMax3 : max; 
		max = (tmpMax4 > max) ? tmpMax4 : max; 
		max = (tmpMax5 > max) ? tmpMax5 : max; 
		max = (tmpMax6 > max) ? tmpMax6 : max; 
		
		return max;
	}

	/**
	 * Given two students, calculate the Euclidean Distance between them
	 * 
	 * @param student1
	 * @param student2
	 * @return 
	 */
	public double getDistance(int student1, int student2) {
		int s1 = student1 < student2 ? student1 : student2;
		int s2 = student1 < student2 ? student2 : student1;

		if (StudentScores.Distance[s1][s2] == -1) {
			int[] score1 = this.getScores(student1);
			int[] score2 = this.getScores(student2);

			double s = 0;
			for (int i = 0; i < score1.length; i++) {
				s += Math.pow(score1[i] - score2[i], 2);
			}
			StudentScores.Distance[s1][s2] = Math.sqrt(s);
		}

		return StudentScores.Distance[s1][s2];

	}
	
